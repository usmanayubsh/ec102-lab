#include <iostream>
using namespace std;
int main()
{
    int jane = 44; // assignment statement
    int harry = 12;

    cout << (jane == harry) << endl;
    cout << (harry <= 12) << endl;
    cout << (jane > harry) << endl;
    cout << (jane >= 44) << endl;
    cout << (harry != 12) << endl;
    cout << (7 < harry) << endl;
    cout << (0) << endl;
    cout << (44) << endl;

    return 0;
}